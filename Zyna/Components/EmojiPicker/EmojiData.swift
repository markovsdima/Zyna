    /// Unicode name lookup for search — built automatically via CFStringTransform.
    static let names: [String: String] = {
        var map = [String: String]()
        for category in categories {
            for emoji in category.emojis {
                map[emoji] = unicodeName(for: emoji)
            }
        }
        return map
    }()

    private static func unicodeName(for emoji: String) -> String? {
        let cfStr = NSMutableString(string: emoji)
        CFStringTransform(cfStr, nil, "Any-Name" as CFString, false)
        // Result format: "\N{GRINNING FACE}" — extract the name
        let result = cfStr as String
        guard result != emoji else { return nil }
        // Strip \N{...} wrappers, may contain multiple for combined emoji
        var names: [String] = []
        result.enumerateSubstrings(in: result.startIndex..., options: .byWords) { word, _, _, _ in
            if let word { names.append(word) }
        }
        return names.joined(separator: " ").lowercased()
    }
